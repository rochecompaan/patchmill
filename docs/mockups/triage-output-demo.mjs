#!/usr/bin/env node

const colourEnabled = !process.env.NO_COLOR && process.stdout.isTTY;

const ansi = {
  dim: "\x1b[2m",
  reset: "\x1b[0m",
  blue: "\x1b[34m",
  cyan: "\x1b[36m",
  green: "\x1b[32m",
  magenta: "\x1b[35m",
  red: "\x1b[31m",
  yellow: "\x1b[33m",
};

function colour(name, value) {
  if (!colourEnabled) return value;
  return `${ansi[name]}${value}${ansi.reset}`;
}

function line(value = "") {
  console.log(value);
}

function field(name) {
  return colour("blue", `${name}:`);
}

function issueNumber(number) {
  return colour("magenta", `#${number}`);
}

function label(value) {
  if (value === "agent-ready") return colour("green", value);
  if (value === "agent-unsuitable") return colour("red", value);
  return value;
}

function truncateLines(lines, maxLines) {
  return lines.slice(0, maxLines);
}

