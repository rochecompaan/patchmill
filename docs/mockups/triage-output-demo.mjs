function printIssue(issue, index) {
  line(
    colour(
      "dim",
      "──────────────────────────────────────────────────────────────────────────────",
    ),
  );
  line(`${issueNumber(issue.number)} ${issue.title}`);
  line(`  ${field("link")} ${colour("cyan", issue.url)}`);
  line(
    `  ${field("labels")} ${colour("dim", issue.labels.before)} ${colour(
      "yellow",
      "->",
    )} ${label(issue.labels.after)}`,
  );

  if (issue.state) {
    line(
      `  ${field("state")} ${colour("dim", issue.state.before)} ${colour(
        "yellow",
        "->",
      )} ${colour("yellow", issue.state.after)}`,
    );
  }

  if (issue.comment) {
    line(`  ${colour("dim", "comment added:")}`);
    for (const commentLine of truncateLines(issue.comment, 5)) {
      line(`  ${colour("dim", commentLine)}`);
    }
  }

  line();
  line(
    `${field("progress")} ${colour("green", `${index + 1}/${run.issueCount} triaged`)}`,
  );
  line();
}

line(`${colour("dim", ">")} ${colour("blue", run.command)}`);
line();
line(`${field("issues")} ${colour("green", String(run.issueCount))}`);
line();

issues.forEach(printIssue);

